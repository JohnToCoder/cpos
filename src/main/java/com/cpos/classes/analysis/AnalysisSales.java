package com.cpos.classes.analysis;

public class AnalysisSales {
    private String gStyle;
    private String gSalesNum;
    private String gSalesPrice;

    public String getgStyle() {
        return gStyle;
    }

    public void setgStyle(String gStyle) {
        this.gStyle = gStyle;
    }

    public String getgSalesNum() {
        return gSalesNum;
    }

    public void setgSalesNum(String gSalesNum) {
        this.gSalesNum = gSalesNum;
    }

    public String getgSalesPrice() {
        return gSalesPrice;
    }

    public void setgSalesPrice(String gSalesPrice) {
        this.gSalesPrice = gSalesPrice;
    }
}
